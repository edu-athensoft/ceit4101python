"""
math module
"""

import math

print(math.ceil(1.5))
print(math.ceil(-1.5))

print(math.floor(1.9))
print(math.floor(-1.9))

print(math.trunc(1.99))
print(math.trunc(-1.99))

print(math.fabs(1.9))
print(math.fabs(-1.9))

print(math.factorial(6))

print(math.degrees(math.pi))
print(math.radians(180))




