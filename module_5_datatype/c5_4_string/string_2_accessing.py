"""
string - accessing char
"""


str1 = 'athensoft inc'
print('str = ', str1)

# accessing char in a string by index
# first character
print('str[0] = ', str1[0])


# last character
print('str[-1] = ', str1[-1])


# slicing - substring
# slicing 2nd to 5th character
print('str[1:5] = ', str1[1:5])


# slicing 6th to 2nd last character
print('str[5:-2] = ', str1[5:-2])
print('str[5:4] = ', str1[5:4])    # empty string



