# string concatenation

str1 = 'Hello'
str2 ='World!'
print(str1, str2)


# + operator
# using +
print("===")
print('str1 + str2 = ', str1 + str2)


# * operator
# using *
print('str1 * 3 =', str1 * 3)


# concatenate without whitespaces
print("===")
str3 = 'Hello ''World!'
print("without space: \t",str3)

# concatenate with whitespaces
str3 = 'Hello '  'World!'
print("with space: \t",str3)

# concatenate spanning multiple-lines
str3 = ('Hello ' 
        'World!')
print("spanning lines: ",str3)


