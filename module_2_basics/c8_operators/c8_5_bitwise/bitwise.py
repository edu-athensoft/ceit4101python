x = 10
y = 4

print("x={}, y={}".format(x,y))

# bitwise AND
print('x & y =', x & y)

# bitwise OR
print('x | y =', x | y)

# bitwise NOT
print('~ x =', ~x)
print('~ y =', ~y)

# bitwise XOR
print('x ^ y =', x ^ y)

# bitwise right shift
print('x >> 2 =', x >> 2)

# bitwise left shift
print('x << 2 =', x << 2)